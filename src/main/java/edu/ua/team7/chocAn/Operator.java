package edu.ua.team7.chocAn;


public class Operator {
    private String name;
    private String id;

    public Operator(String name, String id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public String getId() {
        return id;
    }
}
